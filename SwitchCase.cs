using System;

namespace SwitchCaseDemo {

	class Demo {

		static void Main() {

			for (int i = 0; i < 10; i++) {

				switch (i) {

				case 0:
					Console.WriteLine ("i is Zero");
					break;
				case 1:
					Console.WriteLine ("i is One");
					break;
				case 2:
					Console.WriteLine ("i is Two");
					break;
				case 3:
					Console.WriteLine ("i is Three");
					break;
				case 4:
					Console.WriteLine ("i is Four");
					break;
				case 5:
					Console.WriteLine ("i is Five");
					break;
				case 6:
					Console.WriteLine ("i is Six");
					break;
				default:
					Console.WriteLine ("i is Six and more ...");
					break;

				}


			}


		}

	}
}
